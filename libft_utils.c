#include "libft/libft.h"
#include "ft_printf.h"

void	ft_putfnbr_fd(int n, t_format *fmt, int fd)
{
	int		sign;
	char	c;

	sign = 1;
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		sign = -1;
	}
	else if (fmt->plus)
		ft_putchar_fd('+', fd);
	else if (fmt->space)
		ft_putchar_fd(' ', fd);
	while (fmt->pad--)
		ft_putchar_fd('0', fd);
	if (n / 10)
		ft_putnbr_fd(n / 10 * sign, fd);
	c = '0' + n % 10 * sign;
	ft_putchar_fd(c, fd);
}
